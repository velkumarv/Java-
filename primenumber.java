package week1.day4;

public class primenumber {
public static void main(String[] args) {
	int A= 13;
			int count=0;																																												
			for (int i = 1; i <= A ; i++) {
				if (A%i==0) {
				count++;
				}
			}
			if(count==2) {
				System.out.println("given number is prime numer");
				
			}
			else {
				System.out.println("given numer is not prime number");
			}
}
}
